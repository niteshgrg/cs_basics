/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
var exist = function (board, word) {
  let results = [];
  if (board.length === 0) {
    return false;
  }
  for (let i = 0; i < board.length; i++) {
    for (let j = 0; j < board[0].length; j++) {
      results.push(check(i, j, word, board));
    }
  }

  console.log(results);

  return results.filter((res) => res).length > 0;
};

var check = function (i, j, word, board) {
  if (word === "") {
    return true;
  }
  if (i < 0 || i >= board.length || j < 0 || j >= board[0].length) {
    return false;
  }
  console.log(`${i}${j}`);
  console.log(word);
  let c = board[i][j];

  if (c === word[0]) {
    board[i][j] = "#";
    let newWord = word.slice(1);
    let ret =
      check(i - 1, j, newWord, board) ||
      check(i + 1, j, newWord, board) ||
      check(i, j - 1, newWord, board) ||
      check(i, j + 1, newWord, board);
    if (ret) {
      return ret;
    } else {
      board[i][j] = c;
      return ret;
    }
  } else {
    return false;
  }
};

